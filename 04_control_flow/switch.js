const month = 3

switch (month) {
    case 1:
        console.log("January");
        break;
    case 2:
        console.log("Feb");
        break;
    case 3:
        console.log("March");  // March
        break;
    case 4:
        console.log("April");
        break;

    default:
        console.log("Executes always");
        break;
}